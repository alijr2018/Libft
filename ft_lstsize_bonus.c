#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*cu;
	int		c;

	c = 0;
	cu = lst;
	while (cu != NULL)
	{
		c++;
		cu = cu->next;
	}
	return (c);
}
